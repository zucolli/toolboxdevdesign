    <h2>O que são Fórmulas de Copywriting?</h2>
    <p>Copywriting é a arte de escrever textos persuasivos com o objetivo de vender, engajar ou converter leitores em clientes. As fórmulas são estruturas comprovadas que organizam a mensagem de forma psicologicamente eficaz, independentemente do produto ou nicho.</p>

    <h3>AIDA — Atenção, Interesse, Desejo, Ação</h3>
    <p>A fórmula mais clássica do marketing. <strong>Atenção</strong>: capture o olhar com uma frase impactante sobre o produto. <strong>Interesse</strong>: apresente o benefício principal que desperta curiosidade. <strong>Desejo</strong>: mostre como o produto transforma a vida do cliente, conectando ao problema que ele resolve. <strong>Ação</strong>: conclua com um CTA claro e urgente, incluindo a oferta.</p>

    <h3>PAS — Problema, Agitação, Solução</h3>
    <p>Ideal para produtos que resolvem dores específicas. <strong>Problema</strong>: nomeie a dor do público-alvo. <strong>Agitação</strong>: amplifique as consequências emocionais de não resolver o problema. <strong>Solução</strong>: apresente seu produto como a saída definitiva, reforçando o benefício e a oferta.</p>

    <h3>BAB — Before, After, Bridge</h3>
    <p>Fórmula de transformação. <strong>Before</strong>: descreva a realidade atual do cliente, marcada pelo problema. <strong>After</strong>: pinte o cenário ideal após usar o produto, com o benefício em evidência. <strong>Bridge</strong>: seu produto é a ponte entre os dois mundos, com a oferta como chamada final.</p>

    <h3>Direta / Varejo</h3>
    <p>Para anúncios de resposta imediata em e-commerce e redes sociais. Vai direto ao ponto: produto, benefício, oferta e CTA — sem rodeios. Funciona bem em posts de feed, stories e Google Shopping.</p>

