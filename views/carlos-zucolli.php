        <h2>30 Anos no Varejo Brasileiro</h2>
        <p>Carlos Zucolli iniciou sua trajetória profissional no varejo brasileiro em 1995, período em que a informatização das operações comerciais era uma fronteira sendo desbravada. Desde então, acompanhou — e liderou — a transição do varejo do impresso para o digital, do folheto fotocopiado ao tabloide digital com rastreamento por QR Code e parâmetros UTM.</p>
