    <section class="autor-section">
        <h2>NuAto Comunicação: Tecnologia a Serviço do Varejo</h2>
        <p>A <strong>NuAto Comunicação</strong> é a agência co-fundada por Carlos Zucolli especializada em marketing, tecnologia e design para o setor de varejo. O nome reflete a essência do trabalho: <em>Nu</em> de novo, renovado — <em>Ato</em> de ação concreta, resultado mensurável. A agência opera na interseção entre a comunicação estratégica e a execução técnica de alto volume.</p>
        <p>O portfólio da NuAto inclui clientes que exigem o que há de mais exigente no mercado: gerenciamento simultâneo de múltiplos canais, centenas de SKUs semanais com rastreabilidade total, campanhas de mídia com métricas auditadas por clientes com departamentos de BI internos. Trabalhar nesse nível elimina a tolerância a processos manuais improvisados — é onde nascem ferramentas como as desta Toolbox.</p>
    </section>

    <section class="autor-section">
        <h2>Por Que Criei a Toolbox Dev Design</h2>
        <p>A ideia da Toolbox nasceu de uma frustração recorrente: ferramentas online genéricas que não entendiam os casos de uso reais do varejo. Um gerador de UTMs que não suportava caracteres especiais de nomes de campanhas em português. Um validador de contraste que não considerava texto sobre imagens. Um formatador de JSON que quebrava com feeds de produto de 50 mil SKUs.</p>
        <p>Cada ferramenta nesta coleção foi construída com um cenário de uso real em mente — testada internamente antes de ser disponibilizada publicamente. O código roda 100% no navegador não por comodidade técnica, mas por uma escolha deliberada de privacidade: dados estratégicos de campanhas e catálogos de clientes não devem trafegar por servidores de terceiros.</p>
        <p>Disponibilizar essas ferramentas gratuitamente é também uma forma de devolver à comunidade. Muito do que sei sobre desenvolvimento e marketing digital foi construído sobre conhecimento compartilhado por outros profissionais. A Toolbox é minha contribuição a esse ciclo.</p>
    </section>

