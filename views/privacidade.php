            <li>O uso de cookies analíticos (GA4) e publicitários (AdSense) é baseado no nosso <strong>legítimo interesse</strong> em melhorar o serviço e mantê-lo gratuito.</li>
            <li>Os dados de navegação coletados pelo Google são tratados de acordo com a <a href="https://policies.google.com/privacy" target="_blank" rel="noopener noreferrer">Política de Privacidade do Google</a>.</li>
            <li>Você tem o direito de acessar, corrigir ou solicitar a exclusão de dados pessoais que eventualmente tenhamos sobre você.</li>
