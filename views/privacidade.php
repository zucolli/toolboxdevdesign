        <h2>2. Dados coletados</h2>
        <p>Não coletamos dados pessoais identificáveis (nome, e-mail, CPF etc.). As ferramentas processam as informações inseridas pelo usuário diretamente no navegador (<em>client-side</em>), sem que esses dados sejam transmitidos ou armazenados em nossos servidores.</p>
        <p>A única exceção é a ferramenta <strong>Meu IP</strong>, que realiza uma requisição ao servidor para identificar o endereço IP público do usuário. Esse dado <strong>não é armazenado</strong>.</p>

        <h2>3. Cookies e tecnologias de rastreamento</h2>
        <h3>Google Analytics 4 (GA4)</h3>
        <p>Utilizamos o Google Analytics 4 para coletar métricas de uso anônimas, como páginas visitadas, tempo de sessão e tipo de dispositivo. O GA4 pode usar cookies para distinguir visitas únicas. Você pode optar por não ser rastreado instalando a <a href="https://tools.google.com/dlpage/gaoptout" target="_blank" rel="noopener noreferrer">extensão de desativação do Google Analytics</a>.</p>

        <h3>Google AdSense</h3>
        <p>Utilizamos o Google AdSense para exibir anúncios relevantes. O AdSense pode utilizar cookies para personalizar anúncios com base no seu histórico de navegação. Você pode gerenciar suas preferências de publicidade em <a href="https://adssettings.google.com" target="_blank" rel="noopener noreferrer">adssettings.google.com</a>.</p>

        <h2>4. Conformidade com a LGPD</h2>
        <p>Em conformidade com a Lei Geral de Proteção de Dados (Lei nº 13.709/2018), informamos que:</p>
